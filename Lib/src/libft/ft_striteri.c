#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	i = -1;
	if (s && f)
		while (++i < ft_strlen(s, '\0'))
			f(i, &s[i]);
}
