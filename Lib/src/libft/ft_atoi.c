#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	num;

	sign = 1;
	num = 0;
	if (!ft_strncmp("-2147483648", str, 12))
		return (-2147483648);
	if (!ft_strncmp("2147483647", str, 10))
		return (2147483647);
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
		num = num * 10 + *str++ - 48;
	return (num * sign);
}
